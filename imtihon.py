morse = { ".-": "a", "-...": "b", "-.-.": "c", "-..": "d", ".": "e", "..-.": "f",

"--.": "g", "....": "h", "..": "i", ".---": "j", "-.-": "k", ".-..": "l", "--": "m",

"-.": "n", "---": "o", ".--.": "p", "--.-": "q", ".-.": "r", "...": "s", "-": "t",

"..-": "u", "...-": "v", ".--": "w", "-..-": "x", "-.--": "y", "--..": "z" }


n = input().split("  ")

lst = ""
for i in n:
    harf  = i.split()
    for j in harf:
        for k in morse.keys():
            if j == k:
                lst +=morse[k]
    
    lst+=" " 
    

print(lst)        
